;********************************************************************************************************
; PROCEDURY OBSŁUGI MAGISTRALI I2C
;********************************************************************************************************


;---------------------------------------I2C--------------------------------------------------------------
; PROCEDURA GENERUJĄCA BIT STARTU
;--------------------------------------------------------------------------------------------------------


BITSTART:
	SETB MDE
	SETB MDO
	SETB MCO
	CLR MDO
	CLR MCO
	RET
;---------------------------------------I2C--------------------------------------------------------------
; PROCEDURA GENERUJĄCA BIT STOPU
;--------------------------------------------------------------------------------------------------------

BITSTOP:
	SETB MDE
	CLR MDO
	SETB MCO
	SETB MDO
	RET

;--------------------------------------I2C--------------------------------------------------------------
; PROCEDURA GENERUJĄCA CLOCK+ODBIÓR POTWIERDZENIA, BIT POTWIERDZENIA ZNAJDUJE SIĘ W C
;-------------------------------------------------------------------------------------------------------

CLOCK:
	CLR MDE
	SETB MCO
	MOV C,MDI
	CLR MCO
	RET


;-------------------------------------I2C---------------------------------------------------------------
; PROCEDURA GENERUJĄCA BRAK BITU POTWIERDZENIA
;-------------------------------------------------------------------------------------------------------
NACK:
	SETB MDE
	SETB MDO
	SETB MCO
	CLR MCO
	RET

;-------------------------------------I2C--------------------------------------------------------------
; PROCEDURA GENERUJĄCA BIT POTWIERDZENIA
;------------------------------------------------------------------------------------------------------
ACK:
	SETB MDE
	CLR MDO
	SETB MCO
	CLR MCO
	RET	
	


;-------------------------------------I2C--------------------------------------------------------------
; WYSŁANIE BAJTU, BAJT POBIERANY JEST Z ACC, POTWIERDZENIE OD SLAVE W ZNAJDUJE SIĘ W C
; NISZCZY ACC,B!!!
;------------------------------------------------------------------------------------------------------

WRBAJT:
	MOV B,#08H
WRLOOP:
	RLC A
	SETB MDE
	MOV MDO,C
	SETB MCO
	CLR MCO
	DJNZ B,WRLOOP
	SETB MDO
	ACALL CLOCK
	RET
	

;-------------------------------------I2C----------------------------------------------------------------
; ODBIOR BAJTU, BAJT W ACC, POTWIERDZENIE OD SLAVE W C
;NISZCZY ACC,B!!!
;--------------------------------------------------------------------------------------------------------

RDBAJT:
	SETB MDO
	SETB MDE
	MOV B,#08H
RDLOOP:
	ACALL CLOCK
	RLC A
	DJNZ B,RDLOOP
	RET

;--------------------------------------------------------------------------------------------------------
;WYSŁANIE DANYCH, LICZBA BAJTÓW W BYTECNT, ADRES TABLICY BAJTOW W DPTR, ADRES SLAVE W SLAVE_ADDR
;--------------------------------------------------------------------------------------------------------

SENDDATA:
	ACALL BITSTART
	MOV A,SLAVE_ADDR
	ACALL WRBAJT
SENDLOOP:	
	MOV A,BYTECNT
	MOVC A,@A+DPTR
	ACALL WRBAJT
	DJNZ BYTECNT,SENDLOOP
	ACALL BITSTOP
	RET


;---------------------------------------------------------------------------------------------------------------
; ZAPIS BAJTU DO PAMIECI AT24C128, ADRES W SLAVE_ADDR, WORD1 I WORD2 W WORD_ADDRH/L, BAJT DO WYSLANIA W DATA_OUT
;----------------------------------------------------------------------------------------------------------------

WRFLASH:
	ACALL BITSTART

	MOV A,SLAVE_ADDR
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRERR

	MOV A,WORD_ADDRH
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRERR

	MOV A,WORD_ADDRL
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRERR

	MOV A,DATA_OUT
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRERR

	AJMP WREND
WRERR:
	SETB ERROR
	
WREND:
	ACALL BITSTOP
	ACALL OPZ
	RET


;---------------------------------------------------------------------------------------------------------------
; ODCZYT BAJTU Z PAMIECI AT24C128, ADRES W SLAVE_ADDR, WORD1 I WORD2 W WORD_ADDRHL2, BAJT ODCZYTANY W DATA_IN
;----------------------------------------------------------------------------------------------------------------

RDFLASH:
	ACALL BITSTART

	MOV A,SLAVE_ADDR
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,RDERR

	MOV A,WORD_ADDRH
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,RDERR

	MOV A,WORD_ADDRL
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,RDERR
	
	MOV A,SLAVE_ADDR
	INC A
	ACALL BITSTART
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,RDERR
	ACALL RDBAJT
	MOV DATA_IN,A
	AJMP RDEND	


RDERR:
	SETB ERROR
	
RDEND:
	ACALL NACK
	ACALL BITSTOP
	;ACALL OPZ
	RET


;----------------------------------------------------------------------------------------------------------------
; ZAPIS STRONY PAMIECI (64 BAJTY) DANYMI Z TABLICY WSKAZYWANEJ PRZEZ DPTR
;----------------------------------------------------------------------------------------------------------------


WRPAGE:            
	MOV BYTECNT,#0		; REJESTR ZAWIERA AKTUALNĄ LICZBĘ ZAPISANYCH BAJTÓW DO PAMIECI
		
	ACALL BITSTART		; ROPOCZĘCIE ZAPISYWANIA STRONY
	MOV A,SLAVE_ADDR	;ADRES PAMIĘCI
	ACALL WRBAJT
	MOV ACKBIT,C		;ODCZYTAJ POTWIERDZENIE
	JB ACKBIT,WRPERR	;JESLI NIE MA POTWIERDZENIA TO BŁĄD TRANSMISJI	

	MOV A,WORD_ADDRH	; WYZSZA CZĘŚC ADRESU KOMÓRKI
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRPERR

	MOV A,WORD_ADDRL	;NIŻSZA CZĘŚC ADRESU KOMORKI
	ACALL WRBAJT
	MOV ACKBIT,C
	JB ACKBIT,WRPERR
	MOV A,BYTECNT		
WRPLOOP:
	MOVC A,@A+DPTR
	ACALL WRBAJT
	call opz
	MOV ACKBIT,C
	JB ACKBIT,WRPERR
	INC BYTECNT
	MOV A,BYTECNT
	CJNE A,#64,WRPLOOP
	AJMP WRPEND
	
WRPERR:
	SETB ERROR

WRPEND:
	ACALL BITSTOP
	RET

;--------------------------------------------------------------------------------------------------------------
; ZAPISUJE TABLICĘ KTOREJ ADRES ZNAJDUJE SIE W DPTR, ROZMIAR TABLICY POBIERANY JEST Z AR_SIZE_H I AR_SIZE_L
; ADRES PIERWSZEJ KOMÓRKI W PAMIĘCI POBIERANY JEST Z WORD_ADDRH/L, ADRES PAMIĘCI POBIERANY JEST Z SLAVE_ADDR
; NISZCZY DPTR,ACC,B!!! 
;--------------------------------------------------------------------------------------------------------------

WRARRAY:

	MOV CNTSIZEL,#00H
	MOV CNTSIZEH,#00H

WRARLOOP:

	
	MOV A,#00H
	MOVC A,@A+DPTR
	MOV DATA_OUT,A
