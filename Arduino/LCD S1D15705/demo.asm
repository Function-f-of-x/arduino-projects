LICZ_CZAS: 	

	SETB RS0		;PO KAZDEJ SEKUNDZIE INKREMENTOWANE SĄ REJESTRY SEKUN, MINUT I GODZIN
       INC R0 			;PRZYKLADOWO INKLREMENTACJA REJESTROW MINUT MOZLIWA JEST PO SPELNIENIU
       CJNE R0,#10,ODS 		;WARUNKU TAKIEGO, ZE REJESTRY SEKUND ZAWIERAC BĘDA WARTOSC 6 I 0 (60)
       MOV R0,#0 		;WTEDY INKREMENTUJE SIE REJESTRY MINUT A REJESTRY SEKUND ZERUJE, I.T.D.
       INC R1 
       CJNE R1,#6,ODS 
       MOV R1,#0 
LICZ_MINUTY: 
       INC R2 
       CJNE R2,#10,ODS 
       MOV R2,#0 
       INC R3 
       CJNE R3,#6,ODS 
       MOV R3,#0 
LICZ_GODZINY: 
       INC R4 
       CJNE R4,#4,DAA 
       CJNE R5,#2,DAA 
       MOV R0,#00H 
       MOV R1,#00H 
       MOV R2,#00H 
       MOV R3,#00H 
       MOV R4,#00H 
       MOV R5,#00H 
       AJMP ODS 
DAA:    CJNE R4, #10,ODS 
       MOV R4, #0 
       INC R5 
ODS: 
      CLR RS0
	CALL ODSWIEZ
       RET


